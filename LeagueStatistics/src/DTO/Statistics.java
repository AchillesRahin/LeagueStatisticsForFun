package DTO;

public class Statistics {
	
	private int wins;
	private int losses;
	
	public Statistics(){
		wins = 0;
		losses = 0;
	}
	public int getWins() {
		return wins;
	}
	public void setWins(int wins) {
		this.wins = wins;
	}
	public int getLosses() {
		return losses;
	}
	public void setLosses(int losses) {
		this.losses = losses;
	}
	
	

}
